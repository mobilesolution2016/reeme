return function(app)
    --require('mobdebug').start('192.168.3.15')
	app	:init({})
		:on('pre', function(app, reeme, path, act, mth)
            reeme.log.clear()
		end)
		:on('err', function(app, path, act, errmsg)
		    ngx.header['Content-type'] = 'text/html;charset=utf-8'
		    ngx.say(string.format('load %s.%s controller failed <hr> %s', path, act, errmsg))
		end)
        :on('missmatch', function(app, path, act)
            ngx.say(string.format('%s.%s controller not defined', path, act))
        end)
		:run()
    --require('mobdebug').done()
end